import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '../../..');
const outDir = path.join(root, 'contracts', 'out');
const generatedDir = path.join(root, 'apps', 'deployer', 'src', 'generated');
const generatedFile = path.join(generatedDir, 'contracts.ts');

const artifacts = [
  ['EmberFactory', 'EmberFactory.sol', 'EmberFactory'],
  ['MaintenancePoolFactory', 'MaintenancePoolFactory.sol', 'MaintenancePoolFactory'],
  ['EmberCore', 'EmberCore.sol', 'EmberCore'],
  ['MaintenancePool', 'MaintenancePool.sol', 'MaintenancePool'],
  ['IERC20Token', 'IERC20Token.sol', 'IERC20Token'],
];

function readArtifact([exportName, sourceFile, contractName]) {
  const file = path.join(outDir, sourceFile, `${contractName}.json`);
  if (!fs.existsSync(file)) {
    throw new Error(`Missing ${file}. Run "forge build --force" from contracts/ first.`);
  }
  const artifact = JSON.parse(fs.readFileSync(file, 'utf8'));
  const bytecode = artifact.bytecode?.object ?? '';
  return { exportName, abi: artifact.abi, bytecode };
}

fs.mkdirSync(generatedDir, { recursive: true });

const contracts = Object.fromEntries(
  artifacts.map((artifact) => {
    const item = readArtifact(artifact);
    return [
      item.exportName,
      {
        abi: item.abi,
        bytecode: item.bytecode ? `0x${item.bytecode.replace(/^0x/, '')}` : '0x',
      },
    ];
  }),
);

fs.writeFileSync(
  generatedFile,
  `// Auto-generated by scripts/generate-contracts.mjs. Do not edit by hand.\n` +
    `import type { Abi, Hex } from 'viem';\n\n` +
    `export const contracts = ${JSON.stringify(contracts, null, 2)} as const satisfies Record<string, { abi: Abi; bytecode: Hex }>;\n`,
);

console.log(`Generated ${path.relative(root, generatedFile)}`);
